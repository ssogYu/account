  SUCCESS = 0,

  // 1xxx 通用错误
  UNKNOWN = 1000,
  BAD_REQUEST = 1001,
  UNAUTHORIZED = 1002,
  FORBIDDEN = 1003,
  NOT_FOUND = 1004,
  VALIDATION_FAILED = 1005,
  TOO_MANY_REQUESTS = 1006,
  INTERNAL_ERROR = 1007,

  // 2xxx 用户模块
  USER_NOT_FOUND = 2001,
